<h6>Seja Bem vindo a Secretária Virtual, </h6>
<h1>Officer Assistant</h1>
<table class='table'>
	<tr>
	<th> <p style="text-align: center;">O Officer Assistant é um aplicativo destinado para auxílio do escrituário, no qual sua base de interação seja uma consulta presencial ou até mesmo remota. Tendo seu principal destino a gerência e organização do Consultor/Escrituário.</p><p style="text-align: center;">O O.A. apresenta como principal funcionalidade, a marcação da consulta, não menos importante, o consultor além de conseguir marcar sua consulta com seus clientes, ele conseguirá cadastrá-los em seu banco de dados, conseguirá criar questionários para ter mais afinidades com seus clientes e por fim controlar sua agenda no que tange seu consultório.</p></th></tr></tr>
</table>
<h1>Como uso o Officer Assistant ?</h1>
<table class='table'>
	<tr>
	<th> <p>Para marcar sua consulta, você terá que:</p>
		<p><i class="far fa-check-circle"></i> Cadastrar seu(s) Consultor(es); </p>
		<p><i class="far fa-check-circle"></i> Cadastrar seu(s) Cliente(s);</p>
		<p><i class="far fa-check-circle"></i> Com o Consultor e o Cliente cadastrado, preencha o formulário com as informações completas do cliente;</p>
    <p><i class="far fa-check-circle"></i> Em seguida ao Formulário, monte sua pré-consulta em Agenda;</p>
		<p><i class="far fa-check-circle"></i> Após as etapas anteriores, você pode Marcar sua consulta em efetivo em Marcar Consulta.</p></th></tr></tr>
</table>

